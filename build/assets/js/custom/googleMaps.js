// google maps disable scroll
const handleScrollOnMap = function () {
  let map = document.getElementById('map');
  map.addEventListener('click', disableScrollOnMap);
  map.addEventListener('mouseleave', enableScrollOnMap);
}
const disableScrollOnMap = function (map) {
  map.classList.add('map-clicked');
}
const enableScrollOnMap = function (map) {
  map.classlist.remove('map-clicked');
}
handleScrollOnMap();

